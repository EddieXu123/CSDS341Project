class Student:

    def __init__(self, first_name, last_name, user_id, password, gpa, year, major, DOB, phone_num, gender):
        self.first_name = first_name
        self.last_name = last_name
        self.user_id = user_id
        self.password = password
        self.gpa = gpa
        self.year = year
        self.major = major
        self.DOB = DOB
        self.phone_num = phone_num
        self.gender = gender


